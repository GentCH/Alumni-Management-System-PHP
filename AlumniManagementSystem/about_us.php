<p class="p1">The History</p>
<p class="p2"> INTRODUCTION TO THE INSTITUTE OF POSTGRADUATE STUDIES (IPS) </p>
<p class="p3">The Institute of Postgraduate Studies (IPS) supervises the graduate programs in UMP. This includes administering the admission, appointment of supervisors, candidature reviews, provision of financial assistance and assessment. The centre is headed by a Dean who is assisted by administrative officers and administrative assistants.</p><br />
<p class="p2">GOVERNANCE OF GRADUATE STUDIES</p>
<p class="p3">The Dean of IPS leads the administrative processes relating to graduate studies in UMP. He reports to the Vice-Chancellor and is a member of the Senate of UMP. The policy setting for graduate studies are endorsed by the Senate which are recommended and formulated by the Senate’s Permanent Sub-Committee on Graduate Studies. The Sub Committee oversees the admission, appointment of supervisors and the assessment of graduate candidates.  For routine administrative processes, the Dean of IPS is assisted by Graduate Studies Management Committee that is chaired by the dean and comprise of deputy deans whom are responsible for graduate studies in their respective faculties.</p><br />
<p class="p2">FUNCTION OF IPS</p>
<p class="p3">The function of IPS includes the following:
<ul class="ul2">
	<li>Promoting UMP’s graduate programs</li>
	<li>Facilitate the appointment of supervisor for graduate student</li>
	<li>Oversees the provision of financial assistance</li>
	<li>Coordinate the end of semester appraisal of candidature</li>
	<li>Organise the mid-term candidature review </li>
	<li>Coordinate the assessment for graduation purposes</li>
	<li>Formulate administrative policies pertaining to graduate studies</li>
	<li>Organise activities to enhance graduate students research capabilities</li>
	<li>Organise activities to enhance the supervisory abilities of UMP’s academic staff</li>
</ul>
</p>
